                <strong>Dr. Sudipta Seal</strong> Dr. Sudipta Seal joined the <a target="_blank" href="http://ampac.ucf.edu/">Advanced Materials Processing and Analysis Center</a> (AMPAC) and <a target="_blank" href="http://www.mmae.ucf.edu/">Mechanical, Materials and Aerospace Engineering</a> (MMAE) at the <a target="_blank" href="http://www.ucf.edu/">University of Central Florida</a> in 1997. He has been consistently productive in research, instruction and service to UCF since 1998. He has served as NanoInitiative Coordinator for the Vice-President of Research &amp; Commercialization. He is currently the Director of AMPAC and <a target="_blank" href="http://nanoscience.ucf.edu/">NanoScience Technology Center</a> (NSTC) since 2009. He is a University Distinguished Professor.</p>
		
				<p>He is the recipient of the 2002 Office of Naval Research Young Investigator Award (ONR-YIP). He created the first UCF student chapter of the Electrochemical Society, a century-old society and was honored with the privilege of viewing the ECS historical archives. He's also been selected for the Japan Society of Promotion of Science Award and the Alexander Von Humboldt Fellow, ASM IIM Lecturer award, Royal Soc of Eng - Elected Visiting Professor Fellow at Imperial College of Science, Technology and Medicine, 2009 Invited to the Frontiers of Eng Sym, National Academy of Engineering. He received the Dean's Award for Faculty Excellence (first one) and received both the 2011 UWM CECS Materials Eng Distinguished Alumnus award and UWM GOLD award (alumni of the past decade). He is a Fellow of American Soc of Materials (FASM) and American Association of Advancement of Science (AAAS).</p>
				
