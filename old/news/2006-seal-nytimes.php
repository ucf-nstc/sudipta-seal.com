				<h1>News</h1>
				<h2>Seal's Nanoresearch Quoted in <em>New York Times</em></h2>
				<h3>The Trouble With Photons</h3>
				<p>October 31, 2006</p>
				<p><a href="http://www.nytimes.com/2006/10/31/science/31observ.html?pagewanted=print">Originally published in the <em>New York Times</em></a><br/>
				&raquo; <a rel="lightbox[news]" href="/images/news/nytimes1.jpg" title="Seal's Nanoresearch Quoted in New York Times">View news clipping</a></p>

				<p>Photoreceptor cells in the retina are bombarded with photons — their job, after all, is to convert light to signals that the brain can understand.</p>

				<p>But this constant rain of photons causes problems, too. Photoreceptor cells are highly oxygenated, and the combination of high oxidation and all those photons produces peroxides and other reactive oxygen intermediates, or R.O.I.’s, which can cause the cells to die. These intermediate compounds have been suspected in certain eye diseases, including macular degeneration.</p>

				<p>What if there was a substance that could be put into the eye to consume these R.O.I.’s? Junping Chen of the University of Oklahoma and colleagues have found a candidate, from the automotive and jewelry industries.</p>

				<p>The substance, <a href="http://www.nature.com/nnano/journal/vaop/ncurrent/abs/nnano.2006.91.html" target="new" title="Read the Abstract">they report</a> in the journal <em>Nature Nanotechnology</em>, is cerium oxide, tiny particles of which are made from the rare-earth element cerium. Known as nanoceria particles, they are currently used in the catalytic converters that are part of automobile exhaust systems and in jewelry polishing.</p>

				<p>The researchers incubated rat retinal cells with the particles and found that they prevented increased production of R.O.I.’s. They also injected the particles into the retinas of albino rats with damaged photoreceptors and found that they helped prevent loss of vision.</p>

				<p>The researchers say that they are not sure exactly why nanoceria particles scavenge R.O.I.’s but that with more research they may prove useful in treating eye diseases involving photoreceptor damage. And since R.O.I.’s are thought to be partly responsible for other degenerative diseases including diabetes, the researchers suggest that nanoceria particles should be investigated for use in treating those diseases as well. </p>
