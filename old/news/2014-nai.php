				<h2>Dr. Seal Named Fellow of the National Academy of Inventors</h2>
				<p>Dr. Sudipta Seal has been awarded Fellow status in the National Academy of Inventors. </p>

				<p>U.S. Deputy Commissioner for Patent Operations Andrew Faile inducted the 2013 class at the NAI's 3rd annual conference, held for the first time at the headquarters of the United States Patent and Trademark Office on March 7, 2014. Seal and company were presented with a special trophy and a rosette pin. A plaque listing the name and institution of each NAI Fellow will be on permanent display at the USPTO.</p>

				<p>Collectively, the 2013 Fellows hold more than 5,600 U.S. patents. With their induction, there are now 244 NAI Fellows worldwide, representing 121 universities and non-profit research institutes. </p>

				<p>Election to NAI Fellow status is a high professional distinction accorded to academic inventors who have demonstrated a highly prolific spirit of innovation in creating or facilitating outstanding inventions that have made a tangible impact on quality of life, economic development, and the welfare of society.</p>

				<p>Nominees must be a named inventor on at least one patent issued by the United States Patent and Trademark Office and must be affiliated with a university, nonprofit research institute or other academic entity.</p>

				<p>Academic inventors and innovators elected to the rank of NAI Fellow are nominated by their peers for outstanding contributions to innovation in areas such as patents and licensing, innovative discovery and technology, significant impact on society, and support and enhancement of innovation. </p>

