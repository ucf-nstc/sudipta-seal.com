				<h1>Surface Engineering &amp; NanoTechnology Lab</h1>
				<p>The SNF lab is equipped with multiple instruments to parallely support multitude of research projects in different areas of science and nanotechnology. The lab is equipped with instruments to facilitate and accelerate the synthesis as well as characterization of materials. It is our motto to become self sustained in characterization of materials. In addition to the various instruments, the lab has a full privileged access to the centralized materials facility (<a href="http://www.ampac.ucf.edu/facilities/MCF.php">Materials Characterization Facility</a>) which is situated next to the SNF Lab. MCF is equipped with the most modern and scientific instruments required for characterization of materials and is supported by very promising and dedicated staff which make this facility one of the best in nanomaterials and surface characterization.
				
				<h4>Fundamental Science</h4>
				<ul>
					<li>Exploring the extraordinary chemistry of nanoceria</li>
					<li>Structural evolution of one dimensional and polygonal ceria and titania nanostructures</li> 
					<li>Optical and electrical properties of nano oxides at low temperature</li>
					<li>Active element effect of rare earth in oxidation prevention at high temperature</li>
				</ul>
				
				<h4>Characterization</h4>
				<ul>
					<li>Atomic scale property evaluation</li> 
					<li>Application oriented characterization</li>
					<li>Nanostructure characterization</li>
				</ul>
				
				<h4>Applications</h4>
