				<h1>NSF Research Experiences for Undergraduates</h1>
				<img src="images/photos/reu1.jpg" class="imgleft" alt="Students at Cape Canaveral" />
				<p>The National Science Foundation sponsored program on Research Experiences for Undergraduates (REU)/Research Experiences for Teachers (RET) is sponsored by <a target="_blank" href="http://pegasus.cc.ucf.edu/~ampac/home.html">Advanced Materials Processing and Analysis Center (AMPAC)</a>, <a target="_blank" href="http://www.research.ucf.edu">Office of Research   &amp; Commercialization (ORC)</a> at the <a target="_blank" href="http://www.ucf.edu">University of Central Florida</a> and NSF in Nanotechnology. The students selected for this program will have the opportunity to perform research projects alongside some of the world's foremost researchers for 10 weeks during the summer. The projects cover a range of topics in both engineering and science, including nanomaterials processing and characterization for the development of advanced technology materials - such as, sensors, carbon nanotubes, advanced nanocharacterization tool, materials processing and consolidation into components, miniaturized system. Project descriptions are summarized on the web site. </p>
				
				<img src="images/photos/reu2.jpg" class="imgright" alt="Symposium" />
				<p>Additionally, the REU participants will take specially designed short courses in Materials Science and Engineering, Nanotechnology and Advanced Nanoscale Characterization techniques. Other activities include weekly seminars, industrial visits, a course in Ethics in Research, and "round table" discussions between the REU participants and organizers. </p>
				
				<p>REU participants must prepare and submit a final, detailed research report by the end of the summer program. In addition, REU participants must make a poster presentation on the results of their research at a closing REU symposium. For exceptional projects, students and their advisors are encouraged to submit technical papers and presentations to <a target="_blank" href="http://www.mrs.org/">Materials Research Society (MRS)</a>, <a target="_blank" href="http://www.asm-intl.org/">ASM International</a>, <a target="_blank" href="http://www.tms.org/TMSHome.html">TMS</a>, <a target="_blank" href="http://www.avs.org/right.html">American Vacuum Society (AVS)</a>, <a target="_blank" href="http://www.aps.org/">American Physical Society (APS)</a>, <a target="_blank" href="http://membership.acs.org/c/chas/">American Chemical Society (ACS)</a> and educational related journals.</p>
				
