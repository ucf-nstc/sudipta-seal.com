				<h1>Durip ONR Plasma NanoManufacturing Lab</h1>
				<p>The thermal spray laboratory at UCF houses the state-of-the-art coating and bulk component development facility comprising Plasma and HVOF spray modules. The laboratory is well equipped to develop coatings from ceramic, metallic particles (both micro and nano size ranges) and solution precursors with applications spanning from corrosion-wearoxidation resistant, thermal barrier to thermal protection. The laboratory also has expertise to manufacture bulk nanostructured composites with simple to complex geometries with considerable ease than other processing techniques. Depending on the material and coating/ bulk component properties, the torch and spray parameter combination is chosen from the thermal spray infrastructure. Using a range of plasma spray torches <em>3 MB (40 kW), 9 MB (80 kW), F4 (60 kW), SG100</em> torches, the high melting point materials could be processed. HVOF torches (Jet Kote) is useful to shoot the metallic particles at high velocities thereby high dense overlay coatings could be achieved on desired substrates. The torches are mounted on a six axis robot that could carry the torch to virtually any location in the booth precisely to perform the coating process precisely. Sensors (DPV, Acura Spray, Spray Watch) are available to determine the velocity and temperature of particles during the spray process that can be used to correlate the spray parameters to the particle states and thereby the properties of the end application. Powder feeders (<em>1264 Praxair model and DJ 9MP</em>) and mass flow controllers are available for precise monitoring of the feed rates of powders and gases respectively. Spray drying equipment is available to prepare nano-feedstock powders for effective and safe handling of nano powders for coatings and components for thermal, catalytic, biomedical and energy applications.</p>

				<h4>A few examples of our work:</h4>
				<p><strong>High Temperature Oxidation Resistance coating using solution precursor plasma spray:</strong>
				<br />Solution precursor plasma spray (SPPS) is used to deposit nanoceria coating on steels. Inhouse material characterization facility are available to study the microstructure and the surface chemistry using thermodynamic calculations, X-ray photoelectron spectroscopy (XPS), TEM, SEM, FIB, RAMAN and XRD. Cyclic oxidation at 10000C revealed excellent corrosion resistance of the steel in presence of the SPPS-processed nanoceria coating.</p>
				
				<p><strong>Spray drying of nano alumina and ceramic particles to facilitate plasma spray:</strong>
				<br />Spray drying has been successfully utilized to agglomerate the nano particles to facilitate the handling and uniform melting of the agglomerates during the spray. The process variables could be optimized to achieve narrow size agglomerates with a mean size of 25 μm that could be easily fed through the plasma torch and achieve a nanostructured coating. Spray could be optimized to achieve agglomerates of any nanoparticles that have virtual merits with retained nanostructures in the final coating from the point of view wear resistance, high thermal cycles to failure and low thermal conductivity. </p>
				
				<p><strong>Bulk Nanocomposites:</strong>
				<br />Free form bulk nanocomposites with ceramic and metal matrices with complex geometries have been developed that requires no machining. Some of the properties that have been achieved are high temperature oxidation resistance using MoSi<sub>2</sub>-Si<sub>3</sub>N<sub>4</sub> , high elastic modulus using W-HfC, high fracture toughness (5 MPa.m<sup>1/2</sup>) Ni-Alumina, increase ductility in nano-micro alumina, high fracture toughness Carbon nanotube-Alumina, TaC composites, Al-Si alloys and nano Al-Si components and many others for a variety of high temperature and aerospace applications. </p>
				
