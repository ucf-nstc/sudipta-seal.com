					<li>
						<h2>Nano-bio assembly and system development</h2>
						<div>
							<img src="images/photos/vision.jpg" class="imgright" />
							<p>Using the ordered nature and affinity of bio-molecule pairings, we can template and conjugate nanoparticles into macro, micro or nanosystems.  These technologies typically use dual component compatibility system consisting of two types of bio molecules that act similarly to a lock and key to coordinate different particles coordinated to the "keys" to conjugated with particles or systems containing the "locks."  The strength of the bio-molecule conjugations can allow for particles to be coordinated into formations that can range from 3-d templates, to surface coatings for various energetic applications.
							<p>Applications: nanoparticle diffusion, catalytic, and general kinetics in a variety of material systems including propellants, energetics, solid catalysts, and nano-composites.</p>
						</div>
