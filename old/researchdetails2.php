					<li>
						<h2>Rare Earths for Biomedical Applications - Nanobiotechnology</h2>
						<div>
							<p>Our group has discovered and developed biocompatible rare earth oxide nanoparticles. These are not toxic and well tolerated in both in vitro and in vivo animal models. Our patented rare earth formulation is being applied to a series of biological applications. Of particular interest, specially engineered cerium oxide nanoparticles (CNP) can act as a regenerative antioxidant or pro-oxidant depends on the micro environment and valance state of the nanoparticles. We have shown that specifically engineered CNPs can protect biological tissues against radiation induced damage, can scavenge superoxide anions as well as peroxide, kills cancer cells, prevent neuronal damage, and functionalized CNP can direct and target to disease specific sites and protects cells from insult (Example application: Alzheimer’s, anti-inflammatory, ovarian cancer, delivers drugs, controls stem cell proliferation, prevents retinal damage, facilitate wound healing and can control or reduce the growth and proliferation of tumors, used as additives in bio-scaffolds). Our lab also working on protocols on nanoparticle safety. We are working on fundamental mechanism on nanoparticle-cell interaction in a dynamic cell environment.</p>
							<h3>Funding</h3>
							NIH, DOD, NSF
							<h3>Collaborators</h3>
							Mayo, Henry Ford, Germany, Imperial College-UK, Virginia Tech, IIT-India, Oklahoma Eye Inst, McGill, Duke, MD Andersen Cancer Center, Orlando Health
						</div>
					</li>
					<li>
						<h2>Nanostructure sensor system for chemical and biological detection</h2>
						<div>
							<p>We are exploring the chemi-resistance properties of semiconductor oxide for developing gas sensor by integrating into MEMS device. Our devices are highly sensitive and selective and can operate in low temperature and high humid conditions. We are also investigating various nano-membranes to enhance the performance in high humidity environment. Further, theoretical models are developed explaining the sensing mechanism of reducing gases on nano semiconductor oxides. Detailed material characterization is performed to understand the surfaces of these nanosensors. We are also developing bio-sensors, where the redox property of nanocrystalline cerium oxide is utilized to detect and measure the production of hydrogen peroxide in biological environment. This has implication in detecting ROS in various neurodegenerative disorders. Research is focused on the surface modification of electrode, which plays a key role in such bio-sensor, due to surface contamination by protein molecules present in the environment of sensor applications.</p>
							<h3>Funding</h3>
							NSF
						</div>
					</li>
					<li>
						<h2>Atomistic Simulation</h2>
						<div>
							<p>We have revealed novel radiation sensitive properties of ceria nanoparticles. To understand this, group is working out atomistic simulation to understand the interaction of radiation induced changes in nanocrystalline cerium oxide. Various lattice models are created for cerium oxide and study the effect of radiation by carrying out molecular dynamics simulation. The simulation study is underway in collaboration with the team at Pacific Northwest National Lab. (International partners: Crainfield University)</p>
							<h3>Funding</h3>
							NSF
						</div>
					</li>
					<li>
						<h2>Plasma-based nano manufacturing</h2>
						<div>
							<p>We are developing bulk nano structure coatings and nano composites with metal/CNT and ceramic matrices for functional coatings (corrosion, wear resistant, biomaterials). Recently, we are working on solution precursor plasma spray technique (SPPS) to produce rare earth based functional coatings (with or without dopings). As an example, we have developed doped cerium oxide thin coating for SOFC electrolyte and high temperature oxidation protection. Our research focused on coating development, microstructure structure and properties correlation using advanced material characterization tools.  In house spray drying facility provides variety of spray dried composite powders to develop coatings at different level of porosities.</p>
							<h3>Funding</h3>
							ONR, DURIP, Schlumberger, NSF
							<br />Collaborators: FIU</strong></p>
						</div>
					</li>
					<li>
						<h2>High Temperature Corrosion</h2>
						<div>
							<p>High temperature and high pressure (HT/HP) corrosion test setup has been designed with in situ electrochemical impedance spectroscopy (EIS) capability. The cell can be operated at high pressure (20000 Psi) and high temperature (300 oF) in corrosive conditions, which provides important corrosion information of various steels and materials in HT/HP conditions useful to oil and gas industries.</p>
							<h3>Funding</h3>
							Schlumberger
						</div>
					</li>
					<li>
						<h2>Fundamental Properties of Rare Earth Oxide Nanostructures</h2>
						<div>
							<p>Our group is focused on rare earth oxide nanostructures. We are trying to understand their redox activity at the nanoscale. Especially we are working on Pourbaix type diagram of Nanoceria. Further we are interested in aging of nanoparticles in various solutions. For this we have designed special electrodes to measure redox potential of nanooxides. Final aim is to merge the half cell reaction potential with the peroxide or superoxide reaction system to have a stable spontaneous reaction and further tuning their activity in biological systems. We are also calculating thermodynamic parameters of ceria nanoparticles in various medium for their biological activity. Shape and size of rare earth nanoparticles are studied in high resolution TEM to understand the atomic hoping at select crystallographic planes. In summary, we are trying to understand these oxide nanostructures in solution environment (most of the studies are done in dry state) using Molecular Dynamics and Density Functional Theory calculations.</p>
							<h3>Funding</h3>
							NSF, NIH
							<h3>Collaborators</h3>
							University of Sheffield, Crainfield University
						</div>
					</li>
					<li>
						<h2>Nano Energetics Research</h2>
						<div>
							<p>Composite energetic materials play an important role in modern society, being used in airbags, oil and gas exploration, and most famously, as the propellant in solid rockets. Nanostructured energetic composites have orders of magnitude higher interfacial areas compared to traditional composites, and making the material from the “bottom up”, using molecular building blocks, gives us unprecedented control over the composite structure and composition. Combined, these advantages allow us to dramatically increase the energy density and performance of the material.</p>
